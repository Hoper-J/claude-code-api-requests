/* ============================================================
   Lineage — official CHANGELOG.md → site/public/changelog-data.js
   ------------------------------------------------------------
   Usage:
     node site/scripts/build-changelog.js [path/to/CHANGELOG.md]

   Reads the official anthropics/claude-code changelog snapshot
   (default: corpus/changelog/CHANGELOG.md), keeps only the versions
   present in corpus/manifest.json, and emits:

     window.LINEAGE_CHANGELOG = {
       source, file, url,
       entries: { "<version>": { b: [bullets…], c: {add,fix,imp,chg,oth} } }
     }

   Rules (the UI depends on them):
   - Bullets are quoted VERBATIM — never rewritten, never translated.
   - Category counts are generated by leading-verb classification; the
     UI renders them through locale templates ("3 added" / "新增 3").
   - A corpus version with no heading in CHANGELOG.md simply gets no
     entry; the UI shows the calm "no entry" state.
   zh translations are a separate build artifact — see build-changelog-zh.js.
   ============================================================ */

const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..", "..");
if (process.argv.includes("--help") || process.argv.includes("-h")) {
  console.log(`usage: node site/scripts/build-changelog.js [path/to/CHANGELOG.md]

  Official changelog snapshot (default: corpus/changelog/CHANGELOG.md)
  → site/public/changelog-data.js, filtered to corpus/manifest.json versions,
  with leading-verb category counts. Bullets are quoted verbatim.`);
  process.exit(0);
}
const mdPath = process.argv[2] || path.join(ROOT, "corpus", "changelog", "CHANGELOG.md");
const manifestPath = path.join(ROOT, "corpus", "manifest.json");
const outPath = path.join(ROOT, "site", "public", "changelog-data.js");

const corpusVersions = JSON.parse(fs.readFileSync(manifestPath, "utf8")).versions;

/* ---- parse: "## <version>" headings, "- " bullets, indented continuations ---- */
const md = fs.readFileSync(mdPath, "utf8");
const entries = {};
let cur = null;
for (const line of md.split("\n")) {
  const h = line.match(/^##\s+(\S+)\s*$/);
  if (h) { cur = h[1]; entries[cur] = entries[cur] || []; continue; }
  if (cur === null) continue;
  const b = line.match(/^- (.*)$/);
  if (b) { entries[cur].push(b[1].trim()); continue; }
  if (line.trim() && entries[cur].length) entries[cur][entries[cur].length - 1] += "\n" + line.trim();
}

/* ---- leading-verb classification (digest only — bullets stay verbatim) ---- */
function classify(s) {
  const t = s.replace(/^\[[^\]]+\]\s*/, ""); // strip "[VSCode]"-style scope prefix
  if (/^(fixed|fix\b|bug fixes)/i.test(t)) return "fix";
  if (/^(added|add\b|new\b|introduc|released|launch|shipped|enabled?\b)/i.test(t)) return "add";
  if (/^(improved|improve\b|reduced|faster|optimi[sz]|enhanc|better|performance|sped up|speed up|increased|polish|hardened|quieter|clearer|internal infrastructure)/i.test(t)) return "imp";
  if (/^(changed|updated?\b|renamed|moved|removed|remove\b|deprecat|disabled?\b|stopped|shortened|restored|revert|replaced|default|bumped|lowered|raised|switched)/i.test(t)) return "chg";
  if (/^.{0,80}\bnow\b/i.test(t.split("\n")[0])) return "chg"; // "X now does Y" → behavior change
  return "oth";
}

/* ---- emit, corpus versions only, semver newest-first ---- */
const semverKey = (v) => v.split(".").map((n) => String(n).padStart(4, "0")).join(".");
const out = {};
let withEntry = 0, totalBullets = 0;
for (const v of [...corpusVersions].sort((a, b) => (semverKey(a) < semverKey(b) ? 1 : -1))) {
  const bl = entries[v];
  if (!bl || !bl.length) continue;
  const c = { add: 0, fix: 0, imp: 0, chg: 0, oth: 0 };
  for (const b of bl) c[classify(b)]++;
  out[v] = { b: bl, c };
  withEntry++; totalBullets += bl.length;
}

const payload = {
  source: "anthropics/claude-code",
  file: "CHANGELOG.md",
  url: "https://github.com/anthropics/claude-code/blob/main/CHANGELOG.md",
  entries: out,
};
const js =
  "/* Lineage — official Claude Code release notes, quoted verbatim from anthropics/claude-code CHANGELOG.md.\n" +
  "   Build output of site/scripts/build-changelog.js — covers only versions present in the corpus (" + withEntry + " of " + corpusVersions.length + ").\n" +
  "   Category counts (c) are generated by leading-verb classification; the bullets (b) are untouched English originals. */\n" +
  "window.LINEAGE_CHANGELOG = " + JSON.stringify(payload) + ";\n";
fs.writeFileSync(outPath, js);
console.log("wrote " + path.relative(ROOT, outPath) + " — " + withEntry + "/" + corpusVersions.length + " versions, " + totalBullets + " bullets, " + (js.length / 1024).toFixed(0) + "KB");
